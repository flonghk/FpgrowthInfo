package com.model;

public class FpgrowthModel {

	private String fPName ;
	private int fpCount ;
	private int fpNameCount;
	
	public FpgrowthModel() {
		super();
	}
	
	public String getfPName() {
		return fPName;
	}

	public void setfPName(String fPName) {
		this.fPName = fPName;
	}
	
	public int getfpCount() {
		return fpCount;
	}

	public void setfpCount(int fpCount) {
		this.fpCount = fpCount;
	}
	
	public int getfpNameCount() {
		return fpNameCount;
	}

	public void setfpNameCount(int fpNameCount) {
		this.fpNameCount = fpNameCount;
	}
	
}
